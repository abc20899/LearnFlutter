//////////一个简单的 Dart 程序//////////
// 定义一个函数
void printInteger(int aNumber) {
  print("The number is $aNumber");
}

main() {
  var num = 12;
  printInteger(num); //调用函数

  int lineCount;
  print(lineCount); //null

  const pi = 3.14159;
  const double circle = 2 * pi * 2;
  print(circle);
}
